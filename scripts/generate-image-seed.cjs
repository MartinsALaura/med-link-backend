/*
 * Regenerates the "CATALOG IMAGES" section of src/config/seed.sql.
 *
 * The medication photos are stored in the database (donations.photo) so the app
 * loads them from the API, not from a frontend asset path. This script reads the
 * catalog images bundled in this repo (seed-images/medicamentos), base64-encodes
 * them and appends one UPDATE per donation to seed.sql (idempotently — it
 * replaces any previous generated block). Run after editing the image set:
 *
 *   node scripts/generate-image-seed.cjs
 */
const fs = require('fs');
const path = require('path');

const IMAGES_DIR = path.join(__dirname, '..', 'seed-images', 'medicamentos');
const SEED_FILE = path.join(__dirname, '..', 'src', 'config', 'seed.sql');
const MARKER = '-- ===== CATALOG IMAGES (generated by scripts/generate-image-seed.cjs) =====';

// donation id -> bundled image file (catalog 1-20 + the two pending samples 21-22).
const MAP = [
  [1, 'alenia.webp'], [2, 'Amoxilina.jpg'], [3, 'artroflan.webp'], [4, 'busonid.webp'],
  [5, 'clopidogrel.webp'], [6, 'colidis.webp'], [7, 'dozemast.jpg'], [8, 'forxiga.webp'],
  [9, 'jardiance.jpg'], [10, 'lamitor-cd.webp'], [11, 'levotiroxina.webp'],
  [12, 'mirtazapina.webp'], [13, 'oxalato.jpg'], [14, 'ozempic.webp'], [15, 'prolia.webp'],
  [16, 'prozac.jpg'], [17, 'salbutamol.webp'], [18, 'seretide.webp'],
  [19, 'systane-colirio.webp'], [20, 'torval.webp'],
  [21, 'lamitor-cd.webp'], [22, 'salbutamol.webp'],
];

function mimeFor(file) {
  const ext = path.extname(file).toLowerCase();
  if (ext === '.jpg' || ext === '.jpeg') return 'image/jpeg';
  if (ext === '.png') return 'image/png';
  if (ext === '.webp') return 'image/webp';
  return 'application/octet-stream';
}

const lines = [
  MARKER,
  '-- Real medication images stored as BLOBs so the catalog renders from the DB.',
  '',
];

for (const [id, file] of MAP) {
  const buf = fs.readFileSync(path.join(IMAGES_DIR, file));
  const b64 = buf.toString('base64');
  lines.push(
    `UPDATE donations SET photo = FROM_BASE64('${b64}'), ` +
      `photo_name = '${file}', photo_type = '${mimeFor(file)}' WHERE id = ${id};`
  );
}

let seed = fs.readFileSync(SEED_FILE, 'utf8');
const idx = seed.indexOf(MARKER);
if (idx !== -1) {
  seed = seed.slice(0, idx).replace(/\s+$/, '') + '\n';
}
seed = seed.replace(/\s+$/, '') + '\n\n' + lines.join('\n') + '\n';
fs.writeFileSync(SEED_FILE, seed);

console.log(`Appended ${MAP.length} image UPDATEs to seed.sql (${seed.length} bytes total).`);
